
public class StudentAddress {

    String houseNumber;
    String street;
    int pincode;
    String state;
    String country;

    StudentAddress(String houseNumber, String street, int pincode, String state, String country) {
        this.houseNumber = houseNumber;
        this.street = street;
        this.pincode = pincode;
        this.state = state;
        this.country = country;
    }

}
